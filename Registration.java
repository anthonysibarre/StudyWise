    private void BUTTON_SignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUTTON_SignupActionPerformed
        // TODO add your handling code here:

        String username = TXT_LOGusername.getText().trim();
        String password = new String(TXT_LOGpassword.getPassword());
        String email = TXT_LOGemail.getText().trim();

        if (email.isEmpty()){
            JOptionPane.showMessageDialog(new JFrame(), "Email is required!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (username.isEmpty()){
            JOptionPane.showMessageDialog(new JFrame(), "Username is required!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (password.isEmpty()){
            JOptionPane.showMessageDialog(new JFrame(), "Password is required!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            }
        
        System.out.println("User account created with Username: "+ username);
        
        String DbUrl = "jdbc:mysql://localhost:3306/studywise";
        String DBUsername = "root";
        String DBPassword = "";

         try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection(DbUrl, DBUsername, DBPassword);
        st = con.createStatement();
            
        String queryRegister = "INSERT INTO users (username, password, email, role) VALUES (?, ?, ?, 'client')";
        try (PreparedStatement pstmt = con.prepareStatement(queryRegister)) {
            pstmt.setString(1, username);
            pstmt.setString(2, password);
            pstmt.setString(3, email);
            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(new JFrame(), "Account created successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        
            TXT_LOGusername.setText("");
            TXT_LOGpassword.setText("");
            TXT_LOGemail.setText("");
        }
        
         } catch (ClassNotFoundException | SQLException e) {
        Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, "Database Error", e);
        JOptionPane.showMessageDialog(new JFrame(), "Account creation failed!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BUTTON_SignupActionPerformed

